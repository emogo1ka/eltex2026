#ifndef COUNTER_H
#define COUNTER_H

/**
 * Регистрирует для SIGINT и SIGQUIT одну и ту же функцию-обработчик (sigaction).
 * В обработчике только обновляются флаги; запись в файл выполняется в основном цикле.
 */
void setup_counter_signals(void);

/**
 * Открывает файл, в бесконечном цикле раз в секунду дописывает значение счётчика (1, 2, 3, …).
 * При SIGINT/SIGQUIT в файл записывается строка о получении сигнала (из основного цикла).
 * После третьего SIGINT файл закрывается и функция возвращается.
 */
void counter_run(const char *path);

#endif
