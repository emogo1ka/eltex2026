static unsigned int hash(const char* s) {
	unsigned int h = 0;
	while (*s) {
		h = h * 31 + (unsigned char)*s++;
	}
	return h % HASH_TABLE_SIZE;
}

void init_PhoneBook(PhoneBook* pb) {
    pb->count = 0;
    pb->capacity = 8;
    pb->next_id = 1;

    pb->contacts = malloc(pb->capacity * sizeof(Contact)); //????????? ?????? ??? ?????? ???????? Contact
    if (!pb->contacts) {
        fprintf(stderr, "?????? ????????? ?????? ??? contacts\n");
        exit(1);
    }

	pb->heads = malloc(HASH_TABLE_SIZE * sizeof(int)); //????????? ?????? ??? ?????? "?????" ??? ???-???????
    if (!pb->heads) {
        free(pb->contacts);
        fprintf(stderr, "?????? ????????? ?????? ??? heads\n");
        exit(1);
    }

	pb->next = malloc(pb->capacity * sizeof(int)); //????????? ?????? ??? ?????? ?????????? ?? ????????? ??????? ??? ?????????? ????????
    if (!pb->next) {
        free(pb->contacts);
        free(pb->heads);
        fprintf(stderr, "?????? ????????? ?????? ??? next\n");
        exit(1);
    }

    for (int i = 0; i < HASH_TABLE_SIZE; i++) {
        pb->heads[i] = -1;
    }

    for (int i = 0; i < pb->capacity; i++) {
        pb->next[i] = -1;
    }
}

